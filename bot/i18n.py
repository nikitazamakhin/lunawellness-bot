def get_text(lang: str, key: str, **kwargs) -> str:
    """
    Get translated text by key.

    Args:
        lang: Language code ('ru' or 'uz')
        key: Translation key
        **kwargs: Format parameters for the text

    Returns:
        Translated text with applied formatting
    """
    # Default to Russian if language not found
    translations = TRANSLATIONS.get(lang, TRANSLATIONS["ru"])
    text = translations.get(key, "")

    # Apply formatting if kwargs provided
    if kwargs:
        try:
            return text.format(**kwargs)
        except KeyError:
            # If formatting fails, return text as is
            return text

    return text


def get_direction_text(lang: str, direction: str) -> str:
    """
    Get direction description by direction key.

    Args:
        lang: Language code ('ru' or 'uz')
        direction: Direction key (reformer, aerial, yoga, cycling, shape, spa)

    Returns:
        Direction description text
    """
    key = f"direction_{direction}"
    return get_text(lang, key)


def get_funnel_step_text(lang: str, step: int) -> str:
    """
    Get funnel step text by step number.

    Args:
        lang: Language code ('ru' or 'uz')
        step: Funnel step number (1-10)

    Returns:
        Funnel step text
    """
    key = f"funnel_step_{step}"
    return get_text(lang, key)
